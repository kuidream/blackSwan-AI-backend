# blackSwan Backend Startup Script for Windows PowerShell

Write-Host "Starting blackSwan Backend..." -ForegroundColor Green

# Load environment variables from .env file
if (Test-Path .env) {
    Get-Content .env | ForEach-Object {
        if ($_ -match '^([^#][^=]+)=(.+)$') {
            $name = $matches[1].Trim()
            $value = $matches[2].Trim()
            Set-Item -Path "env:$name" -Value $value
        }
    }
}

# Run server
go run cmd/api/main.go
